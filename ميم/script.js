const uiManager = {
    start() {
        this.getRefs();
        this.setupActions();
        this.loadUserTheme();
        this.loadLang();
    },

    getRefs() {
        this.links = document.querySelectorAll('.nav-item');
        this.views = document.querySelectorAll('.page-view');
        this.btnTheme = document.getElementById('btnThemeSwitch');
        this.btnLang = document.getElementById('btnLangSwitch');
        this.htmlTag = document.documentElement;
        this.menu = document.getElementById('sideNav');
        this.btnMenu = document.getElementById('mobMenuBtn');
        this.dimmer = document.getElementById('dimOverlay');
        this.faqItems = document.querySelectorAll('.accordion-header');
        this.txtSearch = document.getElementById('searchField');
        this.searchView = document.getElementById('search-results-view');
        this.searchArea = document.getElementById('resultsGridArea');
        this.searchMsg = document.getElementById('searchStatusInfo');
        this.btnUp = document.getElementById('btnScrollUp');
    },

    setupActions() {
        this.links.forEach(btn => {
            btn.addEventListener('click', () => {
                const targetId = btn.getAttribute('data-target');
                this.goTo(targetId);
                this.closeNavMobile();
                this.clearSearchBox();
            });
        });

        this.btnTheme.addEventListener('click', () => this.flipMode());
        this.btnLang.addEventListener('click', () => this.flipLang());
        this.btnMenu.addEventListener('click', () => this.toggleNav());
        this.dimmer.addEventListener('click', () => this.closeNavMobile());

        this.faqItems.forEach(header => {
            header.addEventListener('click', function() {
                const block = this.parentElement;
                const bodyText = this.nextElementSibling;
                
                block.classList.toggle('active');
                if (block.classList.contains('active')) {
                    bodyText.style.maxHeight = bodyText.scrollHeight + "px";
                } else {
                    bodyText.style.maxHeight = null;
                }
            });
        });

        this.txtSearch.addEventListener('input', (e) => this.doSearch(e.target.value));

        window.addEventListener('scroll', () => {
            if (window.scrollY > 300) {
                this.btnUp.classList.add('visible');
            } else {
                this.btnUp.classList.remove('visible');
            }
        });

        this.btnUp.addEventListener('click', () => {
            window.scrollTo({ top: 0, behavior: 'smooth' });
        });
    },

    goTo(pageId) {
        this.views.forEach(p => p.classList.remove('active'));
        this.links.forEach(l => l.classList.remove('active'));

        const targetEl = document.getElementById(pageId);
        if (targetEl) targetEl.classList.add('active');

        const activeLink = document.querySelector(`.nav-item[data-target="${pageId}"]`);
        if (activeLink) activeLink.classList.add('active');

        window.scrollTo({ top: 0, behavior: 'smooth' });
    },

    loadUserTheme() {
        const pref = localStorage.getItem('theme');
        if (pref === 'dark') {
            this.htmlTag.setAttribute('data-theme', 'dark');
            this.changeModeIcon('dark');
        }
    },

    flipMode() {
        const currentlyDark = this.htmlTag.getAttribute('data-theme') === 'dark';
        if (currentlyDark) {
            this.htmlTag.removeAttribute('data-theme');
            localStorage.setItem('theme', 'light');
            this.changeModeIcon('light');
        } else {
            this.htmlTag.setAttribute('data-theme', 'dark');
            localStorage.setItem('theme', 'dark');
            this.changeModeIcon('dark');
        }
    },

    changeModeIcon(mode) {
        if (mode === 'dark') {
            this.btnTheme.innerHTML = '<svg viewBox="0 0 24 24"><path d="M12 3c-4.97 0-9 4.03-9 9s4.03 9 9 9 9-4.03 9-9c0-.46-.04-.92-.1-1.36-.98 1.37-2.58 2.26-4.4 2.26-2.98 0-5.4-2.42-5.4-5.4 0-1.81.89-3.42 2.26-4.4-.44-.06-.9-.1-1.36-.1z"/></svg>';
        } else {
            this.btnTheme.innerHTML = '<svg viewBox="0 0 24 24"><path d="M12 7c-2.76 0-5 2.24-5 5s2.24 5 5 5 5-2.24 5-5-2.24-5-5-5zM2 13h2c.55 0 1-.45 1-1s-.45-1-1-1H2c-.55 0-1 .45-1 1s.45 1 1 1zm18 0h2c.55 0 1-.45 1-1s-.45-1-1-1h-2c-.55 0-1 .45-1 1s.45 1 1 1zM11 2v2c0 .55.45 1 1 1s1-.45 1-1V2c0-.55-.45-1-1-1s-1 .45-1 1zm0 18v2c0 .55.45 1 1 1s1-.45 1-1v-2c0-.55-.45-1-1-1s-1 .45-1 1zM5.99 4.58c-.39-.39-1.03-.39-1.41 0-.39.39-.39 1.03 0 1.41l1.06 1.06c.39.39 1.03.39 1.41 0 .39-.39.39-1.03 0-1.41L5.99 4.58zm12.37 12.37c-.39-.39-1.03-.39-1.41 0-.39.39-.39 1.03 0 1.41l1.06 1.06c.39.39 1.03.39 1.41 0 .39-.39.39-1.03 0-1.41l-1.06-1.06zm1.06-10.96c.39-.39.39-1.03 0-1.41-.39-.39-1.03-.39-1.41 0l-1.06 1.06c-.39.39-.39 1.03 0 1.41.39.39 1.03.39 1.41 0l1.06-1.06zM7.05 18.36c.39-.39.39-1.03 0-1.41-.39-.39-1.03-.39-1.41 0l-1.06 1.06c-.39.39-.39 1.03 0 1.41.39.39 1.03.39 1.41 0l1.06-1.06z"/></svg>';
        }
    },

    loadLang() {
        const stored = localStorage.getItem('lang') || 'ar';
        this.applyTranslation(stored);
    },

    flipLang() {
        const current = this.htmlTag.getAttribute('lang');
        const nextLang = current === 'ar' ? 'en' : 'ar';
        this.applyTranslation(nextLang);
    },

    applyTranslation(langCode) {
        this.htmlTag.setAttribute('lang', langCode);
        this.htmlTag.setAttribute('dir', langCode === 'ar' ? 'rtl' : 'ltr');
        localStorage.setItem('lang', langCode);

        document.querySelectorAll('[data-i18n]').forEach(el => {
            const txtKey = el.getAttribute('data-i18n');
            if (siteStrings[langCode] && siteStrings[langCode][txtKey]) {
                el.innerHTML = siteStrings[langCode][txtKey];
            }
        });

        document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
            const pKey = el.getAttribute('data-i18n-placeholder');
            if (siteStrings[langCode] && siteStrings[langCode][pKey]) {
                el.placeholder = siteStrings[langCode][pKey];
            }
        });
    },

    toggleNav() {
        this.menu.classList.toggle('open');
        this.dimmer.classList.toggle('active');
    },

    closeNavMobile() {
        if (window.innerWidth <= 992) {
            this.menu.classList.remove('open');
            this.dimmer.classList.remove('active');
        }
    },

    doSearch(val) {
        const keyword = val.trim().toLowerCase();
        
        if (!keyword) {
            const currentTab = document.querySelector('.nav-item.active').getAttribute('data-target');
            this.goTo(currentTab);
            return;
        }

        this.views.forEach(v => v.classList.remove('active'));
        this.searchView.classList.add('active');
        this.searchArea.innerHTML = ''; 

        const searchBoxes = document.querySelectorAll('.searchable');
        let counter = 0;

        searchBoxes.forEach(item => {
            const words = item.innerText.toLowerCase();
            if (words.includes(keyword)) {
                counter++;
                const copyNode = item.cloneNode(true);
                
                if (copyNode.classList.contains('accordion-item')) {
                    copyNode.style.width = '100%'; 
                    const accBtn = copyNode.querySelector('.accordion-header');
                    accBtn.addEventListener('click', function() {
                        copyNode.classList.toggle('active');
                        const innerPanel = this.nextElementSibling;
                        if (copyNode.classList.contains('active')) {
                            innerPanel.style.maxHeight = innerPanel.scrollHeight + "px";
                        } else {
                            innerPanel.style.maxHeight = null;
                        }
                    });
                }
                this.searchArea.appendChild(copyNode);
            }
        });

        const activeLang = this.htmlTag.getAttribute('lang') || 'ar';
        if (counter === 0) {
            this.searchMsg.innerHTML = activeLang === 'ar' 
                ? `لم يتم العثور على أي نتائج لكلمة: <strong>"${keyword}"</strong>` 
                : `No results found for: <strong>"${keyword}"</strong>`;
        } else {
            this.searchMsg.innerHTML = activeLang === 'ar' 
                ? `تم العثور على <strong>${counter}</strong> نتيجة:` 
                : `Found <strong>${counter}</strong> result(s):`;
        }
    },

    clearSearchBox() {
        this.txtSearch.value = '';
    }
};

function copyBoxData(btnRef, textId) {
    const textNode = document.getElementById(textId);
    const textStr = textNode.innerText; 
    
    navigator.clipboard.writeText(textStr).then(() => {
        const oldHtml = btnRef.innerHTML;
        const pageLang = document.documentElement.getAttribute('lang') || 'ar';
        
        btnRef.innerHTML = pageLang === 'ar' ? '✅ تم النسخ!' : '✅ Copied!';
        btnRef.style.background = 'linear-gradient(135deg, #10b981 0%, #059669 100%)';
        btnRef.style.color = '#ffffff';
        
        setTimeout(() => {
            btnRef.innerHTML = oldHtml;
            btnRef.style.background = ''; 
            btnRef.style.color = '';
        }, 2000);
    }).catch(err => {
        const pageLang = document.documentElement.getAttribute('lang') || 'ar';
        alert(pageLang === 'ar' ? 'حدث خطأ أثناء النسخ، الرجاء المحاولة مرة أخرى.' : 'Error copying text, please try again.');
    });
}

document.addEventListener('DOMContentLoaded', () => {
    if(typeof uiManager !== 'undefined') {
        uiManager.start();
    }

    const videoModal = document.getElementById('videoModal');
    const videoPlayer = document.getElementById('modalVideoPlayer');
    const closeModalBtn = document.querySelector('.close-modal-btn');

    if (!videoModal || !videoPlayer) return;

    document.body.addEventListener('click', function(e) {
        const btn = e.target.closest('.open-video-modal');
        
        if (btn) {
            e.preventDefault();
            
            const videoSrc = btn.getAttribute('data-video'); 
            
            videoPlayer.src = encodeURI(videoSrc);
            
            videoPlayer.load(); 
            
            videoModal.classList.add('show-modal');
            
            setTimeout(() => {
                videoPlayer.play().catch(error => {
                    const pageLang = document.documentElement.getAttribute('lang') || 'ar';
                    console.log("تعذر التشغيل:", error);
                    alert(pageLang === 'ar' ? "المتصفح يمنع التشغيل التلقائي أو المسار غير صحيح." : "Browser blocked autoplay or the file path is incorrect.");
                });
            }, 100);
        }
    });

    function closeModal() {
        videoModal.classList.remove('show-modal');
        videoPlayer.pause(); 
        videoPlayer.src = ''; 
    }

    if(closeModalBtn) closeModalBtn.addEventListener('click', closeModal);

    videoModal.addEventListener('click', function(e) {
        if (e.target === videoModal) {
            closeModal();
        }
    });
});